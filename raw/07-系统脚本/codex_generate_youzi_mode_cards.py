def card(mode: dict) -> str:
    return "\n".join(
        [
            f"# {mode['name']}",
            "",
            "## 模式定义",
            "",
            mode["definition"],
            "",
            "## 适用条件",
            "",
            f"- 适用市场：{mode['market']}",
            f"- 题材阶段：{mode['theme_stage']}",
            f"- 股票角色：{mode['role']}",
            "",
            "## 买入条件",
            "",
            "\n".join(f"- {x}" for x in mode["buy"]),
            "",
            "## 卖出/退出条件",
            "",
            "\n".join(f"- {x}" for x in mode["sell"]),
            "",
            "## 仓位权限",
            "",
            mode["position"],
            "",
            "## 竞价确认",
            "",
            mode["auction"],
            "",
            "## 禁用条件",
            "",
            "\n".join(f"- {x}" for x in mode["forbid"]),
            "",
            "## 常见错误",
            "",
            "\n".join(f"- {x}" for x in mode["mistakes"]),
            "",
            "## 统计字段",
            "",
            "- 市场状态",
            "- 题材阶段",
            "- 股票角色",
            "- 买入方式",
            "- 仓位",
            "- 持股周期",
            "- 盈亏额",
            "- 是否符合竞价确认",
            "- D+1/D+3/D+5表现",
            "",
            "## 来源依据",
            "",
            mode["source"],
            "",
            "## 当前状态",
            "",
            "观察。进入 A 类前必须由用户交割单和候选票 D+ 跟踪验证。",
            "",
        ]
    )


def main() -> None:
    OUT.mkdir(parents=True, exist_ok=True)
    groups = {}
    index = [
        f"# 游资交易模式卡片库索引-{TODAY}",
        "",
        f"模式数量：{len(MODES)}",
        "",
        "这些模式来自高纯度游资心得、淘股吧复盘、公众号方法库和用户已有有效模式。当前统一状态为观察，不能自动视为 A 类。",
        "",
        "| 模式 | 分组 | 适用市场 | 股票角色 | 文件 |",
        "|---|---|---|---|---|",
    ]
    for m in MODES:
        path = OUT / f"{m['name']}.md"
        path.write_text(card(m), encoding="utf-8")
        groups.setdefault(m["group"], []).append(m)
        index.append(f"| {m['name']} | {m['group']} | {m['market']} | {m['role']} | {path.relative_to(ROOT).as_posix()} |")
    index += ["", "## 分组", ""]
    for group, items in sorted(groups.items()):
        index.append(f"### {group}")
        index.append("")
        for m in items:
            index.append(f"- {m['name']}")
        index.append("")
    (OUT / f"游资交易模式卡片库索引-{TODAY}.md").write_text("\n".join(index), encoding="utf-8")
    print(json.dumps({"mode_cards": len(MODES), "root": str(OUT)}, ensure_ascii=False))


if __name__ == "__main__":
    main()
